import React from 'react';
import { RoomType } from '../dataTypes/enums/RoomType.ts';

interface PlaceCardProps {
  id: string;
  price: number;
  type: RoomType;
  image: string;
  title: string;
  onMouseOver: (id: string) => void;
  isPremium?: boolean;
  isFavorite?: boolean;
}

export function OfferCard({
  id,
  price,
  type,
  image,
  title,
  onMouseOver,
  isPremium,
  isFavorite,
}: PlaceCardProps): React.JSX.Element {
  const handleMouseOver = (): void => onMouseOver(id);
  return (
    <article onMouseOver={handleMouseOver} className="cities__card place-card">
      {isPremium && (
        <div className="place-card__mark">
          <span>Premium</span>
        </div>
      )}
      <div className="cities__image-wrapper place-card__image-wrapper">
        <a href="#">
          <img
            className="place-card__image"
            src={`../../markup/img/${image}`}
            width="260"
            height="200"
            alt="Place image"
          />
        </a>
      </div>
      <div className="place-card__info">
        <div className="place-card__price-wrapper">
          <div className="place-card__price">
            <b className="place-card__price-value">&euro;{price}</b>
            <span className="place-card__price-text">&#47;&nbsp;night</span>
          </div>
          <button
            className={`place-card__bookmark-button ${isFavorite ? 'place-card__bookmark-button--active' : ''} button`}
            type="button"
          >
            <svg className="place-card__bookmark-icon" width="18" height="19">
              <use xlinkHref="#icon-bookmark"></use>
            </svg>
            <span className="visually-hidden">
              {isFavorite ? 'In bookmarks' : 'To bookmarks'}
            </span>
          </button>
        </div>
        <div className="place-card__rating rating">
          <div className="place-card__stars rating__stars">
            <span style={{ width: '100%' }}></span>
            <span className="visually-hidden">Rating</span>
          </div>
        </div>
        <h2 className="place-card__name">
          <a href="#">{title}</a>
        </h2>
        <p className="place-card__type">{type}</p>
      </div>
    </article>
  );
}
